void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size) {
    if (huart->Instance == USART1) {

        // 1. Gom dữ liệu gửi về
        if (rx_len_tam + Size <= 128) {
            memcpy(&rx_buffer_tam[rx_len_tam], uart_rx_buf, Size);
            rx_len_tam += Size;
        } else {
            rx_len_tam = 0; // đầy thì reset về 0
        }

        // 2. phân tích gói dữ liệu gửi về
        if (Parse_Packet(rx_buffer_tam, rx_len_tam, &rx_packet)){
            // TÌM THẤY GÓI TIN HOÀN CHỈNH!
            if (rx_packet.mode == 0) {
                Motor_Stop(&motor_left);
                Motor_Stop(&motor_right);
            } else {
                // Cập nhật PID
                pid_balance.Kp = rx_packet.kp;
                pid_balance.Ki = rx_packet.ki;
                pid_balance.Kd = rx_packet.kd;

                // Tránh xe bị giật nảy lên khi đổi hệ số
                pid_balance.integrator = 0.0f;
            }

            rx_len_tam = 0;
        }

        // 3. Cơ chế tự chống kẹt (Auto-flush)
        if (rx_len_tam > 50) {
            rx_len_tam = 0;
        }

        // 4. Mở cửa chờ đón đợt dữ liệu tiếp theo
        HAL_UARTEx_ReceiveToIdle_DMA(&huart1, uart_rx_buf, 64);
    }
}

